export function Sidebar() {
  const pathname = usePathname()
  const { user, organization, logout } = useDashboard()

  return (
    <aside className="w-60 h-screen bg-[#0d0d0d] border-r border-[#1c1c1c] flex flex-col fixed left-0 top-0">
      {/* Logo */}
      <div className="p-5 border-b border-[#1c1c1c]">
        <Link href="/dashboard" className="flex items-center gap-2">
          <span className="text-lg font-bold text-white">Synra</span>
        </Link>
        <p className="text-[11px] text-gray-500 mt-1 truncate">
          {organization?.name || 'Loading...'}
        </p>
      </div>

      {/* Navigation */}
      <nav className="flex-1 p-3 space-y-1 overflow-y-auto">
        {navItems.map((item) => {
          const isActive =
            item.href === '/dashboard'
              ? pathname === '/dashboard'
              : pathname.startsWith(item.href)

          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2 rounded-md text-sm transition-all ${
                isActive
                  ? 'bg-blue-500/10 text-blue-400 border border-blue-500/20'
                  : 'text-gray-400 hover:text-white hover:bg-[#161616] border border-transparent'
              }`}
            >
              {item.icon}
              {item.label}
            </Link>
          )
        })}
      </nav>

      {/* User section */}
      <div className="p-4 border-t border-[#1c1c1c]">
        <div className="flex items-center gap-3 mb-3">
          <div className="w-8 h-8 rounded-full bg-gradient-to-br from-blue-500 to-blue-700 flex items-center justify-center text-white text-xs font-bold">
            {user?.name?.charAt(0)?.toUpperCase() || '?'}
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm text-white truncate">{user?.name}</p>
            <p className="text-[11px] text-gray-500 truncate">{user?.email}</p>
          </div>
        </div>
        <button
          onClick={logout}
          className="w-full px-3 py-1.5 text-xs text-gray-400 hover:text-red-400 hover:bg-red-500/5 border border-transparent hover:border-red-500/20 rounded-md transition-all"
        >
          Sign Out
        </button>
      </div>
    </aside>
  )
}
